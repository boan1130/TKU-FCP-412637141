public class Ch4_5 {
    public static void main(String[] args) {
        boolean a=true, b=false;
        System.out.printf("%b || %b = %b\n",a, b, a||b); //邏輯運算子OR
        System.out.printf("%b && %b = %b\n",a, b, a&&b); //邏輯運算子AND
        System.out.printf("!%b == %b = %b\n",a, !a); ////邏輯運算子NOT
    }
}
